ist bitmap to make top logo oval, the same size.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=856">juniper7</a> — Sun Nov 01, 2015 3:44 am</p><hr />
