You can adjust how long items stay highlighted. There is a hidden Registry setting: &quot;OldProgramsAge&quot; (DWORD value) at HKEY_CURRENT_USER\Software\IvoSoft\ClassicStartMenu\Settings. Set it in Decimals to number of hours for which you want the highlight to remain. The default is 48 hours. Set it for example to 168 hours (1 week) or 336 hours (2 weeks) - any value you think is OK. 5 days would be 120 hours.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Mar 09, 2016 2:19 am</p><hr />
