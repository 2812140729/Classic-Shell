Right click the Start button -&gt; Settings. Check &quot;Show all settings&quot;. On the Controls tab, make sure that &quot;Left click opens&quot; is set to Classic Start Menu.<br /><br />Now go to the General Behavior tab and make sure the option 'Start automatically for this user' is checked.<br /><br />If the menu is still not running after shutdown, then open File Explorer and go to C:\Program Files\Classic Shell. Right click ClassicStartMenu.exe and click Copy.<br /><br />Then in File Explorer, go to C:\Users\&lt;your user name&gt;\AppData\Roaming\Microsoft\Windows\Start Menu\Programs\Startup. Once you are in the Startup folder, right click in an empty area and click Paste shortcut. Now the menu will load every time Windows boots.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Aug 21, 2017 11:17 am</p><hr />
