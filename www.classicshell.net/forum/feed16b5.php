You don't have to edit any skin to get small icons. Open Start Menu settings, All Settings, go to Skin tab and check the option &quot;Small Icons&quot;.<br /><br />To get Downloads (or any folder), you will have to add it first. Go to Customize Start Menu tab, scroll all the way down and add a &quot;Custom Command&quot;. Position it up/down by drag and drop where you want. Double click it and in its Link field, add ' shell:Downloads ' (without the quotes). Check the &quot;Don't expand&quot; checkbox at the bottom and click OK twice.<br /><br />To change Control Panel to categories, on Customize Start Menu tab, locate Control Panel in the column, double click it and from the dropdown, change the Command field to control_panel_categories.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Jan 09, 2013 9:25 am</p><hr />
