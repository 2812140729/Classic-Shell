Notepad may be opening if there is a hidden system file called desktop.ini in your Startup folder. Using Classic Start Menu, go to All Programs -&gt; Startup and right click 'Startup' and click Open and Open All Users. Make sure that hidden files and system files are showing in Explorer. If you don't know how to show them, open Folder Options and go to View tab. Set &quot;Show hidden files, folders and drives&quot; and uncheck &quot;Hide protected operating system files&quot;. Now you can delete Desktop.ini so Notepad won't open at startup.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Oct 18, 2014 5:38 am</p><hr />
