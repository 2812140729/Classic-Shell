By default during remote sessions Restart and Shutdown are disabled. This is done to mimic the behavior of the classic start menu. You can enable them with the setting &quot;Show Shutdown during remote sessions&quot; in the Main Menu tab.<br /><br />By default Log Off should show up. If it doesn't, double-check that the &quot;Show Log Off&quot; setting in the Special Items tab is checked. Also verify that there is a &quot;Log off&quot; menu item in the Customize Start Menu tab. Both need to be set for the command to show.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Wed Dec 24, 2014 10:37 am</p><hr />
