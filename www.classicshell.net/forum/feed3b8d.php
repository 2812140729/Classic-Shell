Well, if anyone's interested the native solution is using High Contrast themes.<br /><br />Pros  - make clock text black<br />Cons - introduces some strange visual problems - system tray icons remain white, but have a black border which in turn has a white border. System tray icons also develop a heavy black border.<br />Having tried changing theme colours it turns out that the white portion of the system tray icon border is linked to the window colour, so changing the colour to classic grey (240, 240, 240,) for  example, leaves you with grey windows. The black border, strangely is controlled by the Window Text colour, but so is the clock font; resulting in a catch 22 situation where you can merge the    borders with the taskbar colour, but it merges the clock as well...<br /><br />I also tried modifying the areolite.msstyles file using msstyler, changing the three font colour values for  Class ID 238: TrayNotify::Clock from white (255,255,255) to black (0,0,0), but this doesn't appear to work for some reason.<br /><br /><br />The following programs work all with windows 10 to replace system clock color<br /><br />DSCLOCK <br /><br /><!-- m --><a class="postlink" href="http://www.dualitysoft.com/dsclock/">http://www.dualitysoft.com/dsclock/</a><!-- m --><br /><br />Pros  - Free. Has a nice range of customisable options including font, font colour, background colour, date/time display format, border etc. Has some other features like reminders<br />Cons - Doesn't replace the system clock, so you have to overlay it meaning it doesn't load until slightly after windows.<br /><br />1stClock<br /><br /><!-- m --><a class="postlink" href="http://www.1stclock.com/">http://www.1stclock.com/</a><!-- m --><br /><br />Pros  - Replaces system clock. Good range of customisable options. Has a bunch of other features like reminders calender etc. Wider range of skins (as opposed to block colour).<br />Cons - Expensive<br /><br />also<br /><br />Free Desktop Clock<br /><br /><!-- m --