This looks like an Explorer limitation in Windows but I found a workaround.<br /><br />If you want to compare results, you can open another blank search window by typing the following command in the Run dialog (Win+R), or by creating a shortcut to the following command:<br /><br /><strong><em>Explorer search-ms:</em></strong><br /><br />Then you can type in its search box and compare the results from the first search.<br /><br />Both will search only indexed locations, unlike if you open a new regular Explorer window and begin searching which would search non-indexed locations too.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Sep 10, 2017 8:58 am</p><hr />
