What you see as &quot;Large Icons&quot;, &quot;Extra Large Icons&quot; etc and other views are all thumbnails in Windows 7 Explorer. Check in Tool menu -&gt; Folder Options or Control Panel -&gt; Folder Options if the option &quot;Always show icons, never thumbnails&quot; is checked. It should be unchecked. Also, another possibility is that your thumbnail cache got corrupted. Start Disk Cleanup from Start search and delete the Thumbnail Cache so Windows will re-create thumbnails in Explorer.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Feb 17, 2013 10:10 pm</p><hr />
