That setting is under control of Windows, not Classic Shell and Explorer has issues remembering its position since Windows 7. You can open Control Panel, press F10 to show the menu bar, right click the Menu bar -&gt; uncheck Lock the toolbars. Now a handle will become visible to drag all toolbars from the left (a faint dotted line to the left of each toolbar).<br /><br />When you mouse over that, the pointer turns into a double headed arrow. Adjust their position and close all Explorer windows. Reopen them and if the position sticks, then lock the toolbars.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Mar 23, 2017 4:10 am</p><hr />
