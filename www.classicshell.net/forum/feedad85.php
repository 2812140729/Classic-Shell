The alignment is under control of Windows. Open any folder like Control Panel or Network Connections which has a menu bar and not the Ribbon. Right click the toolbar/menu bar area and unlock the toolbars, and move the Classic Explorer toolbar to the right along the same row as the menu bar. Then navigate from that Explorer window to a location where Explorer shows the Ribbon like C:\ for example and lock the toolbar. Thereafter, for Ribbon-enabled folders, it should stay on the right.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Nov 03, 2015 8:22 pm</p><hr />
