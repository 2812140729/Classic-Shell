The spacing of the sub-menus in the Aero skin is made to match the spacing of the jumplist and the search list of Windows 7. In Windows 7 the sub-menus use a different selection highlight than the main menu, which is less detailed and needs 2 less pixels. I decided for consistency to use the same highlight graphic in all cases. This makes the height of the menu item 10% larger.<br /><br />BTW, the sub-menu metrics have not changed for some time. I don't have older version of CSM in front of me right now, but I'm almost certain the sub-menus were the same.<br /><br />And as Jcee mentioned, all of this is adjustable if you edit the skin.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Fri Jun 21, 2013 8:25 am</p><hr />
