Try this:<br />Go to the %ALLUSERSPROFILE% folder (usually C:\ProgramData). Create a sub-folder called &quot;Classic Shell&quot;. Inside it create 2 subfolders named &quot;Flags&quot; and &quot;Languages&quot;.<br /><br />Download the attached gd-GB.zip file. Inside it you will find gd-GB.bmp an gd-GB.dll. Put the BMP file and put it in the Flags folder and the DLL in the Languages folder.<br /><br />Go to the Classic Shell settings, open the Language tab, and you will see a new entry for &quot;Scottish Gaelic (United Kingdom)&quot;. Select it, press OK, then log off and log in. The start menu should be in Scottish now.<br /><br />If you confirm that the text appears correct on your end, I will publish this file on the website.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Sat Nov 16, 2013 11:19 am</p><hr />
