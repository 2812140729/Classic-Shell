Due to a bug in Windows, sometimes File Explorer can get confused between addons, especially when there are multiple addons and one of the addons is updated or uninstalled &amp; reinstalled. To fix this, open Registry Editor (Regedit.exe), go to <strong>HKEY_CURRENT_USER</strong>\Software\Microsoft\Internet Explorer\Toolbar\ShellBrowser and delete the ITBar7Layout value. Thereafter, Explorer should not get confused which toolbar it enables/disables. After deleting the ITBar7Layout value, you must disable and enable the Classic Explorer toolbar once again in Explorer via the View menu (on Windows 7) or View tab of the Ribbon (Windows 8 and above).<br /><br />The above steps must be done of course in addition to all the Classic Shell related addons being enabled via Internet Options -&gt; Programs tab -&gt;Manage Add-ons.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Jan 19, 2016 9:00 pm</p><hr />
