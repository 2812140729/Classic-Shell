W7's native shortcut keys have the form Ctl+Alt+&lt;Key&gt;. To start with, once defined, they will only work if in a folder in the Startup folder chain. In my system, I created such a new folder: <strong>C:\ProgramData\Microsoft\Windows\Start Menu\Programs\Hotkeys(Ctl+Alt)</strong>. In this folder, <em>Revo Uninstaller's</em> shortcut will launch when I press Ctl+Alt+R. Given this, I Copied the Revo Uninstaller shortcut to my Desktop, where its Ctl+Alt+R hotkey remained intact. Then I *Deleted* the Revo shortcut from my Hotkeys(Ctl+Alt) folder, and rebooted.<br /><br />After reboot, there remained *only* the Revo shortcut on my Desktop, and Ctl+Alt+R still worked to launch it.<br /><br />I hope this answers your question. If not, I would appreciate a short WinXP tutorial on what you are after, as I do not recall any other WinXP Desktop-launch feature.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=381">RetiredEngineer</a> — Sun Dec 01, 2013 8:59 am</p><hr />
