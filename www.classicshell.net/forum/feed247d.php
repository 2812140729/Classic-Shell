My convention is to assign Ctrl+Shft+&lt;key&gt; to a shortcut which does something within an application, and Ctrl+Shft+&lt;key&gt; to one that has wider scope in its actions.<br /><br />In Windows 7, I have two input languages and three keyboard layouts defined. Recently, I have observed that pressing the / key in WordPerfect occasionally puts a e+acute into a document. When this happened, the keyboard layout had mysteriously changed to Canadian French. The Canadian French keyboard has e+acute on the key marked / ? on a US keyboard. So -- the keyboard layout was getting changed. I have Ctrl+Shft+S defined in WordPerfect: it executes a PerfectScript macro.<br /><br />After a few days of this nonsense, I right-clicked on the Taskbar and chose to show the Language toolbar. EN appeared near the Notification Area.<br /><br />I right-clicked on EN, and chose Settings, then clicked on Advanced language settings. This showed keys assigned to changing input language and keyboard layout Ctrl+Shft and Left Alt.<br /><br />I suspect that at times, when I pressed Ctrl+Shft+S, I waited too long to press the S, so Windows thought I was telling it to change keyboard layout.<br /><br />I clicked on Change key sequence and changed both key sequences to None. So far, my hot keys work and the keyboard layout has not changed.<br /><br />Thank you for the forbearance!<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2399">c.haslam</a> — Sat Dec 07, 2013 7:07 am</p><hr />
