The normal way to uninstall (and hence reinstall) an app is to right-click it from the Start screen, however if this shortcut is missing you can also do it from the App Store.<br /><br />Go into 'Store' and then swipe from the top of the screen (I'm not sure how you do this on non-touch screens? Click in the bottom left corner?). This brings up a menu across the top of the screen with 'Home' and 'Your apps' choices. Click on 'Your apps', and a list of &quot;apps not installed&quot; (but which you have previously installed using your Microsoft logon ID) shows by default. This will be an empty list if you've never uninstalled anything.<br /><br />Note that 'Apps not installed on this PC' is actually a clickable drop-down list - so click it and select 'All apps' instead. The full list of all apps that have ever been installed on your PC then shows - and you can then scroll to the corrupted app in this list. Left-click this app and then select 'Install' from the menu choices that then show at the bottom of the screen (the menu of choices here should be 'Clear', 'Install' or 'View details').<br /><br />Clicking on this 'Install' option will reinstall the app - and this in turn will recreate the missing shortcut in the Start screen (which you will then see once again on the Classic Shell start button in 'Desktop' mode too).<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=857">needemailaddr</a> — Thu Jun 13, 2013 3:21 pm</p><hr />
