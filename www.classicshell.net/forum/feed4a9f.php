It would be nice if all the following complicated procedures <br />were a couple of checkboxes in Classic Start settings :^)<br /><br />Use Group Policy to disable the Lock screen. Open Gpedit.msc, go to Computer Configuration -&gt; Administrative Templates -&gt; Control Panel -&gt; Personalization -&gt; Do not display the lock screen. To &quot;disable&quot; the logon screen, you need to logon automatically. Type: control userpasswords2 into the Start Menu's Search box or the Run dialog and press Enter. In the User Accounts control panel, select the user account with which you want to always logon automatically and then uncheck &quot;User must enter a user name and password to use this computer&quot;. Type and confirm the password and click OK twice. Now your system should boot straight to the desktop without showing a lockscreen or logon screen.<br /><br />Thanks for the info!<br /><br />ff<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=1356">ffinder</a> — Thu Oct 03, 2013 2:40 pm</p><hr />
