Btw you can edit any skin in Resource Hacker to reduce or change the highlight color. The bitmap for highlighting is stored in some bitmap resource inside the skin file (it should be saved as a 32-bit BMP with transparency) and the values to be modified are Main_new_selection and Submenu_new_selection. <br /><br />As Ivo said, the highlight will go away after the default period of 48 hours but there is a hidden setting to modify how long programs stay highlighted: HKCU\Software\IvoSoft\ClassicStartMenu\Settings DWORD value: OldProgramsAge . Value is expressed in decimals as the number of hours to highlight. The highlighting is in fact better implemented than the original menu. As soon as you use the program, the highlight will instantly go away (it didn't do so always in the original Microsoft Start menus). Or it will go away after the OldProramsAge period passes.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Jul 24, 2013 8:32 pm</p><hr />
