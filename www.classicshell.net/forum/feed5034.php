You can also get the 2D XP folder icons back if you want for all folders if you turn off the thumbnails. Save the XP folder icon from shell32.dll as an .ico file. Then go to this registry key by open Regedit.exe: <strong>HKEY_LOCAL_MACHINE\SOFTWARE\Microsoft\Windows\CurrentVersion\Explorer\Shell Icons</strong> and and create a string value called 3 at this key. (3 indicates the 3rd icon, counting from 0 in system shell32.dll which is to be replaced/substituted. For the value data of the value '<strong>3</strong>', enter the path to the XP folder.ico. Now log out and reopen Explorer windows and you will get the XP folder icon. If you have unchecked &quot;Always show icons, never thumbnails&quot; then you will still get thumbnails for some folders with the new style angled icon. If you checked it, you will get the 2D XP folder icon for all folders.<br /><br />The registry change should look like this:<br /><br />Windows Registry Editor Version 5.00<br /><br />[HKEY_LOCAL_MACHINE\SOFTWARE\Microsoft\Windows\CurrentVersion\Explorer\Shell Icons]<br />&quot;3&quot;=&quot;&lt;PATH TO XP FOLDER.ICO&gt;&quot;<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Apr 11, 2014 7:45 am</p><hr />
