This is already possible. Open the Customize Start Menu tab in Classic Start Menu Settings. Add a Custom item and drag it up/down to position it wherever you want i.e. above My Documents, Pictures etc. Double click the Custom item's icon and in the Link: field of the Custom item, specify this: <br /><br />shell:AppsFolder\FileManager_cw5n1h2txyewy!Microsoft.Windows.FileManager  <br />  <br />In the Label: field, specify OneDrive optionally but even if you don't, it should identify it as OneDrive in the Start Menu and the icon should be picked up correctly as well.<br /><br />Click OK twice to save settings.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Oct 18, 2014 12:15 am</p><hr />
