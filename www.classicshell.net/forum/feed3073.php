Yes, it is true that the left column of the skin7 can't be transparent. That's because it needs to host a tree control, which doesn't play nice with transparencies.<br /><br />To get transparency in the rest of the menu, you need to add an alpha channel. Usually the transparency comes from the alpha channel of the background bitmap. However if the background is a solid color (like is the case of the Metro skins), the transparency can come from the alpha channel of the mask bitmap. Currently the mask bitmaps only have R, G and B channels. You need to add alpha and set it to the right level of transparency. Make sure you leave the left column opaque (alpha=255).<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Thu Jun 18, 2015 9:58 pm</p><hr />
