Yes unfortunately, Content View is the only view which shows 32 x 32 sized icons but it shows the text labels on the right.<br /><br />The current sizes (as of Vista and later) are:<br />Extra Large Icons – 256 x 256 pixels <br />Large Icons – 96 x 96 pixels (Automatically rendered by Explorer from 256 version) <br />Medium Icons – 48 x 48 pixels <br />Tiles – 48 x 48 pixels <br />Content – 32 x 32 pixels <br />Small Icons – 16 x 16 pixels <br />List – 16 x 16 pixels <br />Details – 16 x 16 pixels<br /><br />There is no direct way to switch to 32 x 32 icon size and show the labels below but you can switch to Medium icons view using the Toolbar button or Ctrl+Shift+3 and then use this AutoHotkey script (<!-- l --><a class="postlink-local" href="http://www.classicshell.net/forum/download/file.php?id=1443">download/file.php?id=1443</a><!-- l -->) to reduce the size quickly. Run it and then can use the Ctrl+ '+/-' keys to zoom in/zoom out icon sizes in Explorer when the focus is on the file pane. That is the best option available right now since directly switching to 32 x 32 icon size with text labels showing below the icons is not possible.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Dec 04, 2015 1:44 am</p><hr />
