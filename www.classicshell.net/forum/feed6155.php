The &quot;Override glass color&quot; setting only affects the Start Menu and only for those skins which use glass or the &quot;Aero&quot; color set on a system-wide level from Personalization -&gt; Window Color. It doesn't change the Taskbar color or title bar color.<br /><br />The color is expressed in hex values but if you click the [...] button, you can also specify RGB or HSL values using the standard Windows color picker dialog. Or you can visually select a color.<br /><br />Hover over the setting and read its tooltip to understand how to configure it.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Apr 12, 2015 6:27 am</p><hr />
