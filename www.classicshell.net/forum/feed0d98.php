If you click the Start button, then in the run/search box type a UNC path (e.g. \\foobar).  When you press enter ClassicShell attempts to access this network resource, but there is no visual feedback to the user that it's doing anything.  If the network resource is inaccessible then ClassicShell sits there for 15 seconds with no visual feedback.  This leaves the user wondering if they actually hit the enter key or not.  Compare this to the Windows Start menu, when you press enter immediately the Start menu closes letting the user know they did indeed press the enter key.  ClassicShell should do something similar, either close the Start menu, or even something as simple as display the &quot;working in the background&quot; cursor.<br /><br />This is a minor gripe for such as awesome program.  But I have close to a dozen computers in my office and often times machines are turned off, so I run into it a lot.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=243">freezer</a> â€” Fri Feb 01, 2013 3:52 pm</p><hr />
