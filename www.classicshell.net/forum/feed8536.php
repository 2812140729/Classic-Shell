Hi, to explain more.<br />It's a little bit tricky because Classic Shell does not dynamically adjust the width of that section of the taskbar. The taskbar image is static. <br /><br />Everybody's desktop will be different and requires a different image size to fit whatever icons reside there.<br />Here are two Win 10 taskbar images, for regular display 96 dpi large/small taskbar.<br /><br />When the Vertical stretching Border size is set to &quot;0,0&quot;<br />the whole height can stretch evenly.<br />For the Horizontal stretching, we need to protect the right side of the image from stretching distortion.<br /><br />In my case, after taking a screenshot, and measuring in a photo editor,<br />I need 218 pixels protected area, so I make image longer/shorter to match.<br />I then input Horizontal stretching Border size &quot;0,219&quot;<br /><br />EDIT: I probably should have made the angled section to be a little darker,<br />like alpha channel 80 or 90. <br />I'll leave that to you, as you are the master creator. <img src="http://www.classicshell.net/forum/images/smilies/icon_e_smile.gif" alt=":)" title="Smile" /><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=856">juniper7</a> — Fri Mar 23, 2018 7:12 am</p><hr />
