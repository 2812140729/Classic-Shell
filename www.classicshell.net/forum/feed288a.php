The program's installer (or API provided by Windows to create the link) does the job of creating the actual shortcuts. The Classic Start Menu only presents them as they are. If it were to rename them automatically, the uninstaller wouldn't be able to remove the shortcut and you would be left with dead/broken shortcuts after the program was uninstalled.<br /><br />The Classic Start Menu has a feature to highlight newly installed programs for this purpose so it becomes very easy to locate what just got installed. Once you find the shortcut, you can simply drag it outside the folder with the company name or rename it to the name of the program itself. (Renaming or moving still breaks the uninstaller).<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Jan 08, 2016 11:30 pm</p><hr />
