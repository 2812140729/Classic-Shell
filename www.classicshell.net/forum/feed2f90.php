By default the Windows 7 style uses a new Pinned folder to store the pinned programs. It is empty by default.<br /><br />You have 2 options - if you have any interesting programs pinned in older versions, they will be in the Start Menu folder (currently will show inside the All Programs list). There is an option to show the Start Menu folder on the main menu like before. In the Main Menu tab change the &quot;Pinned Programs folder&quot; setting to &quot;Use Start Menu folder&quot;.<br /><br />If you don't care about your old pinned programs, pin some new ones. Right-click on a shortcut and select &quot;Pin to start menu (Classic Shell)&quot; or drag the shortcut to the start button, wait for the menu to open and drop the shortcut inside.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Wed Oct 30, 2013 8:58 am</p><hr />
