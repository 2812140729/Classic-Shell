To troubleshoot Classic Shell installation or uninstallation process, there is a separate tool called Classic Shell Utility. It can create troubleshooting logs, remove your Classic Shell settings or completely erase all traces of Classic Shell from your system. It also has a feature to show the Metro color palette for creating skins.<br /><br />For those who are having problems uninstalling or upgrading Classic Shell, the feature to remove Classic Shell from your computer in case the normal uninstall process doesn't work can be useful.<br /><br />The utility will manually delete the installed files and will repair the registry. There are options to keep or delete all your settings.<br /><br />You can download the utility from here: <strong><a href="http://www.classicshell.net/downloads/" class="postlink">http://www.classicshell.net/downloads/</a></strong><br /><br /><img src="http://www.classicshell.net/images/utility.png" alt="" /><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Tue Aug 18, 2015 10:51 pm</p><hr />
