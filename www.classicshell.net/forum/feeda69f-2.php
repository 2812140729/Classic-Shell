There may be many reasons what is causing the delay. One possibility especially if the slowdowns started after upgrading to Windows 8 is that your anti-virus may be blocking C:\Program Files\Classic Shell\ClassicStartMenu.exe as a false positive (some AVs have been reported to cause slowdowns) and therefore causing the delay. Check in your AV settings if there is a setting to exclude processes and add ClassicStartMenu.exe and Explorer.exe to it. The built-in Windows Defender in Windows 8 is a full-fledged anti-virus now which may be conflicting.<br /><br />Are any of the items in your Tools folder located on a network share? Then it is likely to cause delays. Also, if any of your tools include <a href="http://www.autohotkey.com/" class="postlink">AutoHotkey-compiled</a> EXE scripts, then Windows Defender has a known hanging issue with it.<br /><br />The slowdown may also be due to icon caching. Follow the performance tips here to speed things up more: <a href="http://www.classicshell.net/faq/#perf_startup" class="postlink">http://www.classicshell.net/faq/#perf_startup</a>.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Apr 23, 2013 7:18 pm</p><hr />
