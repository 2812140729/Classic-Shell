This options tells the Start Menu to show &quot;Update and shut down&quot;/&quot;Update and restart&quot; commands in the Shutdown submenu. When any of them are run, Windows updates are installed before shutting down and then the OS shuts down/restarts. (Restart functionality is only available in Windows 8+). This functionality of installing updates before shutting down was also there in the Windows 7/Vista/XP original Start Menu. It's not something that Classic Shell does. It just calls a Windows API which performs the process of installing updates. If your Windows Update automatic checking settings are set to &quot;Never check for updates&quot; it may not work. <br /><br />Also, you get no choice to select which updates to install. Windows decides that on its own, whether it installs only critical updates or recommended updates as well, depending on your Windows Update settings in the Control Panel.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri May 30, 2014 10:50 pm</p><hr />
