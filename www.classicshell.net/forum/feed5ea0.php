You can Create a New Button for the standard View Properties found in Organize &gt; Folder and Search Options:<br /><br /><ul><li>Classic Explorer Settings &gt; Toolbar Buttons Tab</li><li>Drag &quot;Custom Command&quot; from the right window to the left window</li><li>Right-click CustomItem &gt; Rename to &quot;FolderOptions&quot;</li><li> Right-click &quot;FolderOptions&quot; &gt; Edit Item<br /><ul><li>Command: %windir%\System32\rundll32.exe shell32.dll,Options_RunDLL 7</li><li>Icon: imageres.dll, 166</li></ul></li><li>Click OK, Restart Explorer</li><li>Done<br /></li></ul><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=359">Tenzen</a> — Wed Mar 20, 2013 9:46 pm</p><hr />
