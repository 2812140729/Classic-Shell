Yep.<br />1. Right click the Start Button, click Settings, enable &quot;All Settings&quot; radio button at the bottom.<br />2. Go to Customize Start Menu tab. In the right column, scroll all the way down.<br />3. Double click the last item, &quot;Custom Command&quot; to add it to the left column. Drag to position it wherever you want, rename it SkyDriveItem<br />4. Now double click CustomItem you just added in the left column to edit it. In its Link field, specify:    %userprofile%\SkyDrive<br />5. If you want to show it as a Link (not as an expanding cascading menu), check the &quot;Don't Expand&quot; checkbox below.<br />6. Click OK twice to save settings.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Apr 20, 2013 5:39 am</p><hr />
