All versions of Windows behave like this since Windows 95. Since it is the installer that creates the shortcut, most installers put shortcuts where the developer has intended them to go. As a workaround, you can open the Properties of the folder or the shortcut and from the General tab, set the &quot;Hidden&quot; attribute and click OK. Then it will still get created but it will be hidden from the Start menu.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Oct 27, 2016 7:39 am</p><hr />
