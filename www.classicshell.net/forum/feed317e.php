<content type="html" xml:base="http://www.classicshell.net/forum/viewtopic.php?t=4359&amp;p=18175#p18175"><![CDATA[
So as you may be knowing, Windows Picture and Fax Viewer in Windows XP was great for viewing GIFs, especially animated GIFs. Windows Vista and later versions of Windows do not support viewing animated GIFs at all in the Photo Viewer. Instead you are supposed to open animated GIFs in a web browser to view them. This is unacceptable. So here is an AutoIt script compiled into an EXE file which serves as the replacement for XP's GIF Viewer:<br /><br />1. Download GIFView.zip<br />2. Extract the EXE to some folder like %appdata%\GIFView\. Do NOT run the EXE directly.<br />3. Instead, right click a GIF file, choose Open With from the right click menu -&gt; &quot;Choose default program&quot;. In the &quot;Open With&quot; dialog, click Browse and select the EXE you downloaded. Associate GIF files to always open with this EXE you downloaded by checking the box &quot;Always use the...&quot;.<br /><br />Now your animated GIF files will play fine in it. Also, you can quickly navigate forward/back and see all the GIF files in a folder.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Mar 13, 2015 2:41 am</p><hr />
