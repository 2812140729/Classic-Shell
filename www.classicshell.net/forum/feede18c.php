That's a great idea. You can add it yourself as follows:<br /><br />1. Open Classic Explorer Settings. Go to Toolbar Buttons tab.<br />2. Drag a custom command and in the Link field, type: shell:Links. Leave the &quot;Command:&quot; field empty.<br />3. Pick the Favorites icon from imageres.dll, 1024.<br />4. Optionally enter the Label and Tip if you want them to show on the toolbar. Click OK to save settings.<br /><br />Your Favorites button is ready. It will list the contents of Explorer Favorites. If you want it to list IE Favorites, enter shell:Favorites instead of shell:Links. Also, you can type shell:Links or shell:Favorites into the Start Menu's search box and hit Enter to open them in Explorer. You can create subfolders to organize favorites, add any kind of program or URL shortcuts.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Sep 25, 2013 3:58 am</p><hr />
