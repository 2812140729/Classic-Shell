Every skin decides how much of the glass color to use and how to mix it with its own color. On one end you have the &quot;full glass&quot; skin which uses the glass color at almost 100%, and on the other you have &quot;metro&quot; and &quot;midnight&quot;, which ignore the glass color almost completely.<br />It is designed to make all of the skin elements be readable and with enough contrast.<br /><br />In this example, the &quot;metro&quot; skin uses the system metro colors for its elements - background, text, selection, various highlights. Those colors are picked to work well together. Changing the background color arbitrarily will make them not match anymore. For example if you pick a white color the text wont be readable.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Thu Aug 06, 2015 8:16 am</p><hr />
