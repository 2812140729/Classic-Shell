To exit the Classic Shell's Start Menu manually, right click the Start button and click Exit.  To exit it automatically (for use in scripts or a batch file), the cmd line switch is:<br /><br />&quot;C:\Program Files\Classic Shell\ClassicStartMenu.exe&quot; -exit<br /><br />In fact, terminating ClassicStartMenu.exe won't help. To exit properly, use the above method only.<br /><br />Other command line parameters are:<br /><br />-open  (opens the Classic Start Menu)<br />-toggle  (toggles the state of the Start menu; opens it if it's closed and vice versa)<br />-togglenew  (toggles the Windows Start menu/Start screen)<br />-settings  (brings up the settings dialog)<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> â€” Mon Oct 28, 2013 1:26 am</p><hr />
