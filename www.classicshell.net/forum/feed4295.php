Once you edit/touch any setting it becomes &quot;modified&quot; from the default and is shown in bold, and that means it will be exported. If you want only modified/changed settings to be exported, either do &quot;Reset all settings&quot; from the Backup button, makes the changes you want and then export them. Or you can restore individual settings to their defaults by right clicking the setting and choosing &quot;Set default&quot;. When it becomes un-bolded, it means it won't be exported.<br /><br />To update your config file, the recommended way is to load the earlier one, make changes and then save it again. As long as you click Cancel in the settings window, they won't be applied, so the settings window also acts as a graphical XML settings editor, where you can load any set of settings, make changes and overwrite the saved one.<br /><br />To disable auto-update checking, open &quot;Classic Shell Update&quot; component from Start Menu folder, and disable the check.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Mar 27, 2013 8:07 am</p><hr />
