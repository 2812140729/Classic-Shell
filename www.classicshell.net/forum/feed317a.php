To keep the left side of the menu small but increase the padding on the right side, determine which skin you are using by going to the Skin tab in Classic Start Menu settings. Also, note the Start Menu style you are using. Windows 7 style skins use the .skin7 extension.<br /><br />Now copy the skin you are using from C:\Program Files\Classic Shell\Skins to another folder like, e.g the Desktop folder. Rename the skin to something else.<br /><br />Download and install the freeware Resource Hacker, Open the skin file on the desktop in Resource Hacker. Press Ctrl+F in Resource Hacker and locate the lines Main2_text_padding. Change the 2nd and 4th values of this Main2_text_padding value (which indicate the top and bottom padding). Click Compile Script button after incrementing the values, save changes to the skin file and copy it back to C:\Program Files\Classic Shell Skins. Switch to the new skin from the Skins tab.<br /><br />If you are not comfortable with skin editing yourself, tell us which Start Menu style and skin you are using.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Aug 13, 2015 8:41 am</p><hr />
