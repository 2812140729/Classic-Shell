You need to do 3 things.<br /><br />First, you need to edit the background bitmaps to increase the bottom blue area. The bitmaps are: 1, 2, 5, 6, 8, 9, 20, 21, 22, 23, 24, 25. Why so many? Because there are 3 color variants and 4 styles. Let's say you add N pixels to the bottom of each bitmap.<br /><br />Second, you need to increase the last number of Main_bitmap_slices_Y by N (from 8 to N+8). Main_bitmap_slices_Y appears in 2 places in the skin.<br /><br />And third, you need to increase the bottom menu padding. It is the last number in Main_padding and Main2_padding (both appear twice in the skin). Increase it from 9 to N+9.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Sat Jan 18, 2014 6:34 pm</p><hr />
