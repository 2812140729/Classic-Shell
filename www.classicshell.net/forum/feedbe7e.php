Do you mean for the Classic Start Menu or system-wide? For Classic Start Menu, go to Menu Look tab in settings and change the &quot;Font smoothing&quot; to what you want. If you are familiar with Resource Hacker, you can edit any Classic Shell skin to customize features like the font it uses to some other font which is not anti-aliased. Open the .skin file from C:\Program Files\Classic Shell\Skins in an elevated instance of Resource Hacker (running as admin), make the changes to fonts and save the skin.<br /><br />By default, Windows Vista and later systems unfortunately use Segoe UI which is heavily anti-aliased and ClearType-optimized. ClearType cannot be turned off entirely for the Windows Shell and Windows Explorer. Parts of the user interface which Classic Shell does not replace still use ClearType regardless of setting. (<!-- m --><a class="postlink" href="http://blogs.msdn.com/b/e7/archive/2009/06/23/engineering-changes-to-cleartype-in-windows-7.aspx">http://blogs.msdn.com/b/e7/archive/2009 ... ows-7.aspx</a><!-- m -->). You can change the Segoe UI font to Tahoma or other from the registry (search on the internet how) but MS says then the layout of dialog boxes and buttons may be skewed.<br /> <br />You could try the powertoy version of ClearType Tuner: <a href="http://www.microsoft.com/typography/ClearTypePowerToy.mspx" class="postlink">http://www.microsoft.com/typography/ClearTypePowerToy.mspx</a> which offers more manual control. Ignore the unsupported warnings and choose to run it anyway.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Feb 28, 2013 12:24 am</p><hr />
