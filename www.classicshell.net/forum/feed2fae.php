There are 2 ways to disable keyboard accelerators (underlined shortcut keys). The keyboard shortcuts you are referring to are called accelerators and are stored in an INI file. I will show you two ways to disable them. You can decide what method works best for you.<br /><br />Method I is permanent and Method II will only work till you upgrade your Classic Shell version, following which you will have to do it again.<br /><br />Method I (Using the settings dialog):<br />1. Open Start Menu Settings by right clicking the Start button.<br />2. Enable &quot;All Settings&quot; radio button at the bottom of the settings window and go to the &quot;Customize Start Menu&quot; tab.<br />3. Locate and double click the icon of the item for which you want to disable keyboard accelerators (underlined shortcuts).<br />4. In the dialog that comes up, erase the existing contents of the &quot;Label&quot; field and type the new name of the command in the &quot;Label&quot; field as you want it to appear. This will make it override the keyboard accelerators defined by the ampersand character ('&amp;') in the INI file. e.g. for ShutDownBoxItem, the label you should enter should be: Shut Down.... For RestartItem, erase '$Menu.Restart' and instead enter just Restart in the 'Label' field. You get the idea. Do this for whichever items you want to disable accelerators.<br /><br />Method II (Edit the INI file directly):<br />1. Open Notepad as administrator by right clicking its shortcut, choosing Run as administrator.<br />2. From Notepad's File menu, click Open.<br />3. Open this file: C:\Program Files\Classic Shell\StartMenuL10N.ini<br />4. From the Edit menu of Notepad, choose Replace (Ctrl+H).<br />5. In the &quot;Find what:&quot; field, enter: &amp; and leave the &quot;Replace with:&quot; field empty. This means we want to delete all instances of '&amp;' in this file so that accelerators get disabled.<br />6. Click Replace All and save changes to the file, close it.<br />7. Right click the Start button, choose Exit and start the menu again from C:\Program Files\Classic Shell\ClassicStartMenu.exe.<br /><br />Method II will only work as long as that INI file is not replaced again. When you update Classic Shell, that INI file will be replaced and they will return. So you will have to do it again. Method I will keep them disabled across version upgrades because settings are preserved in the registry but for each item, you will have to disable it individually by entering a custom Label.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Jul 14, 2013 10:59 pm</p><hr />
