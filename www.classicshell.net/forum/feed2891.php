<title type="html"><![CDATA[One-Click Launch / Main In Folder]]></title>

<content type="html" xml:base="http://www.classicshell.net/forum/viewtopic.php?t=1795&amp;p=9136#p9136"><![CDATA[
The basic idea is that most applications come with a start menu folder with multiple items (the application, uninstall, help, faq, website, etc.) Usually there's only one item that you actually care about. So, by selecting an item and setting it as &quot;Main in folder&quot; when you click on the containing folder, that item will be launched without actually needing to navigate into the folder. The folder icon is also replaced by the icon of the &quot;Main in folder&quot; item. Hovering over the folder will still open the folder in the normal way, so if you need to access the other contents, you can. This was a great time saving feature of Vista Start Menu / Start Menu X which is otherwise slow and a memory hog. I'd love to see this implemented in Classic Shell.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2408">enki1337</a> — Wed Nov 27, 2013 10:20 am</p><hr />
