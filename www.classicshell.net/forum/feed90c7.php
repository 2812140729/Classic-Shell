No you are unlikely to have problems with updates. Unless Microsoft frequently and radically change the OS (which they do only with Windows 10), Classic Shell will continue to work. Note that Classic Shell is like a regular app that uses APIs - a Start menu app and shell extensions for Explorer+IE. The Start menu component hooks into Explorer.exe process, only for as long as it is running in memory. When you exit the Start Menu or disable the shell addons, Windows returns to its original behavior. <br /><br />So it is not possible for Classic Shell to screw up the Windows OS in a harmful way. It does not modify any Windows system files or settings or do any permanent or unsafe modifications. There should be no interference in any way with the updating mechanism of Windows.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Feb 08, 2016 8:03 pm</p><hr />
