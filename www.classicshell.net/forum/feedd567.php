The Metro Glass skin will follow the taskbar/system Aero color: <a href="http://www.classicshell.net/forum/viewtopic.php?f=17&amp;t=110" class="postlink">http://www.classicshell.net/forum/viewtopic.php?f=17&amp;t=110</a> <br />Here's what you can additionally do: in Classic Shell version 3.9.3 and later, there is an &quot;Override Glass Color&quot; setting on the &quot;Menu Look&quot; tab. Don't touch the Glass color as by default it is &quot;Automatic&quot; meaning the color will follow the system Aero color. But it doesn't match the exact shade because the intensity and blending are different. You can tweak the intensity and blending values to make the menu color with the Metro glass skin match the taskbar color.<br /><br />Depending on the skin you use and its skin options, and whether your taskbar is transparent or opaque, different values of intensity and blending will be required to make the menu color match the taskbar Aero color.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Aug 25, 2013 11:54 pm</p><hr />
