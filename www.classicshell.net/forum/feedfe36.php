Moving the button to the right will break Fitt's law for most users so it's a bad idea to leave space to the left of the button <img src="http://www.classicshell.net/forum/images/smilies/icon_e_smile.gif" alt=":)" title="Smile" />. Those who prefer to have it such that shoving the mouse pointer into the bottom left corner opens the menu won't like it. Having an option is not justified because there are already enough ways to prevent the bottom left popup from accidentally opening. One is that you assign Shift+left click or Shift+winkey to open the Start screen (or middle click) and disable the popup. Or you can tell the other admin to access the Start screen from the Charms (Win+C). Note that the Start screen popup is not shown when the user hovers over the entire Start button, it only appears when the mouse moves near the screen's hot corner. Another thing you can do is create a custom larger/wider Start button. Look in the help file on how to create the custom Start button. The width of the button image determines the size. If you make it wide/large enough, the Start screen popup will be farther away.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Jan 18, 2013 1:55 am</p><hr />
