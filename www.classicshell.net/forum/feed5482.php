First, make sure hibernation is enabled by running this command from an elevated cmd prompt: powercfg /hibernate on<br /><br />If you wish to make Hibernate the default option in the Shutdown box (the classic Shutdown dialog), open Group Policy (Gpedit.msc -&gt; User Configuration -&gt; Start Menu and Taskbar -&gt; Change Start Menu power button. Set the action there.<br /><br />You can also make Hibernate the default option in the Start menu itself. For that, open Start Menu settings -&gt; All settings -&gt; Customize Start Menu tab. Locate and double click &quot;ShutdownBoxItem&quot; and from the &quot;Command:&quot; field dropdown, choose 'hibernate'.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Jan 14, 2013 3:47 am</p><hr />
