Well, technically, you can add folders under '(My) Computer' too if you've hidden Favorites and Libraries and HomeGroup. Right click in an empty area of 'Computer' -&gt; Add a network location -&gt; Choose a custom network location. Specify there the folder in the UNC style convention. e.g. Suppose your computer name is Windows-PC and you want to add C:\MyFolder, then specify it as:<br />\\Windows-PC\C$\MyFolder     and finish the wizard. It will be added under 'Computer'.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Apr 20, 2013 5:49 am</p><hr />
