Put the icons anywhere you want. For example, you can create a folder under C:\Users\[your user name]\AppData\Roaming\ClassicShell\ called Icons. Then from Start Menu settings, you must switch to the Classic style or Classic with two columns style which is what is required to use these Royale skins. Go to the Customize Start Menu tab in Settings and double click the Shutdown and Logoff items to bring up the &quot;Edit item&quot; dialog, where you can change their icons. <br /><br />To make the menu more like XP, you can drag the Log off item outside of the Shutdown dialog. Also you can change the <strong>Command</strong> of the Shutdown dialog from shutdown_box to shutdown if you want. Everything is customizable in the Classic styles.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Apr 10, 2014 10:35 pm</p><hr />
