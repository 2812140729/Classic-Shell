The Start button is automatically added on Windows 8, the settings are automatically opened the first time the Start button is clicked where you can choose your style. From that same window, you can enable &quot;All Settings&quot; and you get access to all settings. Also, Classic Shell creates shortcuts to the settings inside &quot;All Programs&quot;. If the menu is not running, the settings related shortcuts show on the Start screen as well on Windows 8. <img src="http://www.classicshell.net/forum/images/smilies/icon_e_smile.gif" alt=":)" title="Smile" /> Plus, you can right click the Start button to access settings. So there are many ways to access settings. Maybe because you installed Win8 Start button first before Classic Shell, something went wrong and you didn't get the settings window and the Start button the first time it was installed. <img src="http://www.classicshell.net/forum/images/smilies/icon_e_smile.gif" alt=":)" title="Smile" /><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Jan 26, 2013 8:42 am</p><hr />
