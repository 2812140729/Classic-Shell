Open Classic Explorer Settings and go to the Toolbar Buttons tab. In the left pane, double click the command for which you want text to be shown and enter it in the Label: field. You can also copy the text from the Tip: field to the Label: field. Whichever buttons have labels, for those buttons the text is shown at the bottom. There is also a setting on the Toolbar Settings tab called &quot;Text is on the size&quot; which shows the text next to the icon.<br /><br />You can get the icons in the screenshot here: <!-- m --><a class="postlink" href="http://www.glyfx.com/content/page/vista-common-toolbar-set-complete-edition.html">http://www.glyfx.com/content/page/vista ... ition.html</a><!-- m --><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Apr 23, 2014 10:04 pm</p><hr />
