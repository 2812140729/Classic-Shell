Classic Start Menu supports keyboard accelerators which you see as underlined letters. To see them, press the Alt key when the menu is open. You can make the underlined letters always show by going to Control Panel -&gt; Ease of Access Center -&gt; Make the keyboard easier to use -&gt; Check the option &quot;Underline keyboard shortcuts and access keys&quot;.<br /><br />When the focus is in the search box, you can press Alt+underlined letter to access the respective item. If the focus is not in the search  box, then just press the underlined letter without Alt.<br /><br />You can define your own keyboard accelerators and also modify the default ones. Put an ampersand character ('&amp;') in the name of any item and it will act as the keyboard accelerator. e.g. &amp;Run means Alt+R will open Run while the focus is in the search box (or only R when focus isn't in the search box).<br /><br />You can also go to the &quot;Search Box&quot; tab in Classic Start Menu settings and uncheck &quot;Selected by default&quot; so you can use accelerators without having to press Alt. The search box can then be accessed by pressing Tab.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Oct 23, 2015 9:54 pm</p><hr />
