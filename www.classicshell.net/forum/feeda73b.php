In the style of the menu that you are using, the menu height should automatically increase as you increase the number of frequently used programs. Open Classic Start Menu Settings and in the search box at the top of the Settings window, type: max. Change &quot;Max recent programs&quot; value to increase the menu height.<br /><br />However, pager arrows will appear at the top and bottom of the list if you increase it too much. In that case, putting the pinned items inside a folder is a good idea.<br /><br />In Windows Explorer (File Explorer), go to this location:<br /><br />C:\Users\&lt;YOUR USER NAME&gt;\AppData\Roaming\Microsoft\Windows\Start Menu<br /><br />You can create any folders there and cut-paste (move) the existing pinned shortcuts inside them.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Apr 22, 2017 7:46 am</p><hr />
