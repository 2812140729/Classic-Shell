There is a new hidden setting added in Classic Shell 4.0.4 to change the jumplist width of the Start Menu in Windows 7 style: HKCU\Software\IvoSoft\ClassicStartMenu\Settings. DWORD value: JumplistWidth. The value data should be entered in decimals. Delete the value to revert to the default jumplist width.<br /><br />This setting is also there in Classic Shell's Group Policy settings: &quot;Jumplist pane width&quot;.<br /><br />You must exit the Start Menu and start it again from C:\Program Files\Classic Shell\ClassicStartMenu.exe.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Jan 20, 2014 2:06 am</p><hr />
