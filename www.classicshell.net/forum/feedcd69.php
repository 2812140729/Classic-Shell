Not sure if anyone here knows this, but in some later beta builds of Windows XP, the flag logo on the login screen was actually animated. After accidentally stumbling upon the original image for that animation, I decided to turn it into an animated Start Button. I made 2 versions, one with the original 50-frame animation, and another with the animation extended to 250 frames, just shy of the max Classic Shell supports. Unfortunately, I had to speed up the 250-frame version, since...well...you can't exactly set the green channel to 300 <img src="http://www.classicshell.net/forum/images/smilies/icon_lol.gif" alt=":lol:" title="Laughing" /><br /><br />The original animation had a white background for some reason, and I will admit that I didn't do an amazing job of removing it. If anyone wants to do better, I can provide the original image (though you can get it yourself if you have Windows XP - just look in C:\WINDOWS\system32\shell32.dll , in the BITMAP section ._. )<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=9454">Splitwirez</a> — Thu Mar 10, 2016 10:04 am</p><hr />
