In Windows 7, Windows 8 and Vista, folder view settings are saved for 5 different folder &quot;templates&quot; depending on their content. So open five folders, one each of the 5 types - containing General/Miscellaneous files, Documents, Pictures, Music, and Videos. Set the view to whatever you want, then open Folder Options from *that folder*. Folder Options is located under Tools Menu (Windows 7/Vista) or the View tab on the Ribbon (Windows 8). In Folder Options, switch to View tab and click &quot;Apply to all folders&quot;. This should make it save the setting for all folders of that content type. Repeat this once for each of these five folders types.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Feb 12, 2013 11:10 pm</p><hr />
