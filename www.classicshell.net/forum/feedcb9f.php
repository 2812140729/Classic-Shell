In case you did install the freeware AutoHotkey, paste this into Notepad and save as 'Scroll to bottom.ahk' or anything you want and create a toolbar button for it.<br /><br /><div class="codetitle"><b>Code:</b></div><div class="codecontent">#NoTrayIcon<br />#Persistent<br />#SingleInstance, Force<br />#IfWinActive ahk_class CabinetWClass<br />Send, {End}<br /></div><br /><br />You can also right click the saved AHK and compile as EXE. Change End to Home for 'Scroll to top.ahk'.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Nov 12, 2013 8:17 am</p><hr />
