For Start Menu, open Classic Start Menu Settings and go to the &quot;Search Box&quot; tab. Uncheck the option &quot;Search contents&quot; which appears below &quot;Search files&quot;. <br /><br />For Explorer, open Folder Options/File Explorer Options. Go to the Search tab and uncheck &quot;Always search file names and contents&quot;. <br /><br />Windows still indexes contents of certain file types. To stop that, go to Indexing Options (in Control Panel) and click the Advanced button. Go to the File Types tab and set all extensions to &quot;Index Properties only&quot;.    <br />  <br />To exclude a particular file or folder's contents from being indexed, open its Properties and go to the General tab. Click Advanced and uncheck &quot;Allow this file to have contents indexed in addition to file properties&quot;. Note that this only excludes that specific file's contents from being indexed unlike the Control Panel option which can prevent indexing of contents on a per file type/extension basis.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Jan 31, 2016 7:45 am</p><hr />
