Microsoft doesn't officially allow theming the rest of the OS. There is an app called <a href="http://uxstyle.com/" class="postlink">UxStyle</a> which unlocks the theming ability, then you can install custom visual styles/themes made by the Windows community from sites like deviantart.com. Maybe you will find a similar visual style/theme as &quot;Full Glass&quot; on devianart.com. Install <a href="http://uxstyle.com/" class="postlink">UxStyle</a> first and then find and apply the theme from DA.<br /><br />Also the theme you install must match the exact version of the Windows version it was made for. Meaning Windows 7 visual styles won't work with Windows 8 and vice versa.<br /><br />Disclaimer: Classic Shell is not responsible for anything that happens to your PC from installing third party apps.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Jun 15, 2014 8:35 pm</p><hr />
