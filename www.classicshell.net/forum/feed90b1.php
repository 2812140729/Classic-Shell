<content type="html" xml:base="http://www.classicshell.net/forum/viewtopic.php?t=3317&amp;p=15469#p15469"><![CDATA[
At the moment, the dropdown &quot;standard commands&quot; for start menu items includes entries for &quot;shutdown&quot; and &quot;restart&quot;.<br /><br />The trouble is that Windows automatically changes these to &quot;Update and shutdown&quot; and &quot;Update and restart&quot; (and doesn't show the simple &quot;shutdown&quot; and &quot;restart&quot; options) if Windows Updates are available. Some users may want the wording and action to change, but others may not want this behaviour. Fixing this isn't hard if one knows a little (I use the item entry &quot;shutdown -p -t 0&quot; and &quot;shutdown -r -t 0&quot; or for forced shutdown, add &quot;/f&quot;) but this isn't obviously possible unless one happens to know a shutdown command exists. So it might be easier and more intuitive if the list were explicit and clearly allowed a choice whether shutdown items should change wordings should change or not, if updates exist.<br /><br />It's hard to word new dropdown entries concisely, without being confusing (I tried).<br /><br />So could the dialog instead have a checkbox added for &quot;Don't change to 'update and...' &quot; or &quot;Change to &quot;Update and...&quot; if updates exist&quot; (or something), which allows the user to choose behaviour and item wording, if Windows Updates exist?<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=1934">stilez</a> — Sat Nov 01, 2014 9:01 pm</p><hr />
