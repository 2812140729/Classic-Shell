The toolbar you see below the search box that says &quot;Search This PC&quot; is the Classic Explorer toolbar with buttons for Cut, Copy, Paste, Delete, Properties, Email and Classic Explorer settings. Since the Ribbon above it takes a vast amount of space, you can minimize it by pressing Ctrl+F1. Then you can click the button for Classic Explorer settings and add any commands you want. Check the box in Classic Explorer settings called &quot;Show All Settings&quot; and then go to the Toolbar Buttons tab. On the Toolbar Buttons tab, the right column lists all the available commands and the left one is where you must drag them from the right to add the ones you want. Once you drag the command to the left, you can double click it in the left column to edit the label (name), command, tip etc. If you don't want the labels to show, keep the label field empty.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Nov 18, 2013 5:27 am</p><hr />
