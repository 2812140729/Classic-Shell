Taskbar on any other side of the screen except the bottom interferes with several UI elements which follow Fitts' law: the Close button of a maximized window in top right corner, scroll bars on the right edge, title bars/tabs in a browser like Chrome if at the top, and the window menu (Alt+Space) which can be used to close a program with double click.<br /><br />Previously, Windows supported toolbars on other sides but they were only used to launch applications, not show running programs on different sides. The Classic Shell Start Menu is more than enough to substitute for the loss of toolbars on the screen's sides. Classic Start Menu has rich customization and ability to organize a complex hierarchy of folders using submenus.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Jan 21, 2017 7:34 am</p><hr />
