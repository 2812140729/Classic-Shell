If the IE addon is disabled, and yet IE isn't working then something else busted it. You could do System Restore to restore to an earlier date. Do you have any software such as skin packs or any unofficial mod which patches/modifies system files? That could be the cause.<br /><br /><br />Classic Shell doesn't modify system files or affect other system functionality. Run the Classic Shell installer again, then select Change. Uncheck the Classic IE9 component to uninstall it. See if that gets IE9 going. It is unlikely Classic Shell is the problem though. It may be some incompatibility between multiple programs.<br /><br />If after uninstalling Classic IE component, IE9 does not start, it means the problem is unlikely to be related to Classic Shell as it only includes an addon for IE which is removed when that component is uninstalled. IE is not modified in any other way.<br /><br />To revert to 3.6.8, open Programs and Features Control Panel and uninstall Classic Shell 4.0. Then reinstall 3.6.8 by downloading its installer again from the Sourceforge website.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Oct 25, 2013 4:47 am</p><hr />
