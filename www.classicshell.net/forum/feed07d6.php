The rules are:<br />1) If you move stuff within the Programs menu, the default operation is MOVE<br />2) If you move stuff from the Programs menu to the main menu, the default operation is SHORTCUT, which in case you are dragging a shortcut is the same as COPY<br />3) At any time you can hold down Shift to force MOVE, hold down Ctrl to force COPY, or both for SHORTCUT<br /><br />The difference between 1 and 2 was done few versions ago to mimic the Windows 7 menu's Pin behavior. When you pin something it doesn't disappear from the original location. Many people were messing up their start menu by trying to pin programs and losing the original shortcut.<br /><br />I am very surprised the Shift key doesn't work for you. I tried moving something from Programs\Adobe to the main menu while holding Shift, and it moved just fine.<br /><br />The mouse cursor should indicate what operation is going to happen - MOVE, COPY or SHORTCUT. Does change change when you press Shift or Ctrl as you are dragging?<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Sat Jan 12, 2013 9:56 am</p><hr />
