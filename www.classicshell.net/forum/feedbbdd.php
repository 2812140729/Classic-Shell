You cant necessarily add 'all' the bookmarks, but you can add individual ones via drag/drop <br />Just highlight the url in the address bar and drag directly into the start menu<br />Alternatively, if you want the link to be on the left side, you can create a custom menu item, and paste the link as the 'target' or 'link<br /><br />Right click the start button, go to settings, Check 'show all settings' on the top, then Customize start menu<br /><br />In windows 7 style:<br />Right click and select 'insert new item'<br />Click where it says 'administrative Tools' on your new link and select 'custom'<br />Double click on the 'icon' which should be a white 'page' at this point<br />Paste the URL into the 'link field'<br />Give it a name<br />Give it an Icon if applicable<br />Move it where in the menu you want it<br /><br />In the classic/2 column styles:<br /> Click on 'custom' in the right column(available commands) to add it to the left column (your menu)<br />Move it where in the menu you want it<br />Click it to edit<br />Paste the URL into the 'link field'<br />Give it a name<br />Give it an Icon if applicable<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=62">Jcee</a> — Wed May 16, 2018 7:13 pm</p><hr />
