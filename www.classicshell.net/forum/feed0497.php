Classic Start Menu has its own &quot;User picture&quot; setting on the General Behavior tab of Settings. If you have customized it but the file no longer exists then it can show a black picture. Right click that setting and choose &quot;Set default&quot;. Then the default user picture will be shown.<br /><br />There was a bug related to the user picture frame showing in Classic Shell 4.2.5 skins on high DPI displays. Download the latest beta from <!-- m --><a class="postlink" href="http://www.classicshell.net">http://www.classicshell.net</a><!-- m -->. Then when you uncheck it on the &quot;Skins&quot; tab, the user picture will be hidden. It will be hidden only in the Start menu, not anywhere else in Windows.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Jun 15, 2016 10:38 am</p><hr />
