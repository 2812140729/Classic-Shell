The registry equivalent to &quot;Remember each folder's view settings&quot; is still intact. MS just took out the option from Folder Options. Go to HKEY_CURRENT_USER\Software\Microsoft\Windows\CurrentVersion\Explorer\Advanced. Add a DWORD value called ClassicViewState and set it to 1. Log off and log on back for it to take effect. Note that this doesn't affect the navigation pane. That ability was removed in Vista (My Computer and Windows Explorer are one and the same now). So if navigation pane is turned off for one folder, it will be closed for all folders that open afterwards.<br />However, you can add a quick button to toggle the navigation pane to the built-in Explorer command bar using a tool like <a href="http://winaero.com/comment.php?comment.news.16" class="postlink">Explorer Toolbar Editor</a>.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat Jan 26, 2013 10:21 am</p><hr />
