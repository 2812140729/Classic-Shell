You must choose the same style that you want to use in your GPO settings. If you want to use Windows 7 style, first open Classic Start Menu Settings and go the first tab called &quot;Start Menu Style&quot;. Pick Windows 7 style there. Then go to the Customize Start Menu tab and add or remove items as you want.<br /><br />In the Registry, the value you need to copy for Windows 7 style is: MenuItems7, not MenuItems1.<br /><br />MenuItems1=Classic single column or just &quot;Classic style&quot;<br />MenuItems2=Classic with two columns<br />MenuItems7=Windows 7 style<br /><br />Throughout all steps, you must pick a style and stick with it.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Mar 24, 2017 7:13 am</p><hr />
