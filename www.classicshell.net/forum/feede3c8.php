Maybe your Pinned programs folder setting and Recent programs setting got reset or changed.<br /><br />To fix this, open Classic Start Menu Settings. In the search box at the top of the Settings window, type: pinned. Without closing the Start Menu Settings, switch the setting between &quot;Start menu folder&quot; and &quot;Pinned folder&quot;. Open the start menu by clicking the Start button and see if your pinned items return.<br /><br />Next, in the Search box of the *Start Menu Settings window*, type: recent. Choose between &quot;Recent&quot; or &quot;Frequent&quot; programs. It should not be set to &quot;Don't show&quot;.<br /><br />Also, if you choose Frequent programs, you need a few more settings turned on at the operating system level.<br /><br />For Windows 10 Creators Update, there is a newly introduced option in the <span style="text-decoration: underline"><em><strong>operating system's</strong></em></span> Settings app -&gt; Privacy: &quot;Let Windows track app launches to improve start and search results&quot;. Turn it on.<br /><br />Then go to Settings -&gt; Personalization -&gt; Start and turn on &quot;Show most used apps&quot;. After turning these on, your Start Menu frequent programs list will be functional once again.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Aug 16, 2017 11:51 pm</p><hr />
