Your problem is related to Virtual PC, not Start Menu related. VM=Virtual machine. What you are using as &quot;XP Mode&quot; is a Virtual Machine running on Windows Virtual PC software which is available <strong>only for</strong> Windows 7. Okay, try this:<br /><br />1. First shutdown Windows XP Mode.<br />2. Open &quot;Windows Virtual PC&quot; from the Start menu.<br />3. Select 'XP Mode' in that folder and click &quot;Settings&quot; button on the toolbar.<br />4. Go to the &quot;Auto Publish&quot; section and make sure the option is checked.<br /><img src="http://blogs.msdn.com/blogfiles/virtual_pc_guy/WindowsLiveWriter/UnderstandingAutoPublishinginWindowsVirt_4C8/autopublish-XPModeSettings_2.png" alt="" /><br /><br />5. Start XP Mode so any shortcuts will be auto-published to the Start Menu folder.<br /><br />XP Mode shortcuts most definitely work for me this way. They show up in %appdata%\Microsoft\Windows\Start Menu\Programs\Windows Virtual PC\Windows XP Mode Applications<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Feb 03, 2013 1:58 am</p><hr />
