1. Check that your Documents folder (C:\Users\&lt;username\Documents) is either added to Control Panel -&gt; Indexing Options. Or include it inside the Documents Library. Doing either of this will make sure the contents are indexed.<br /><br />2. For Word documents, you need to install a Filter Pack. Although Office generally installs it, you can install it manually too from here: <!-- m --><a class="postlink" href="http://www.microsoft.com/en-in/download/details.aspx?id=17062">http://www.microsoft.com/en-in/download ... x?id=17062</a><!-- m --><br /><br />3. Make sure Windows Search indexing is turned on and working correctly. If you open Control Panel -&gt; Indexing Options, it will show you if its off. The &quot;Windows Search&quot; service must be running and set to Automatic in Services.msc.<br /><br />4. The file that you are trying to find must have the index attribute enabled from Properties. Open the file's Properties and go to the General tab. Click the Advanced button and check that &quot;Allow this file to have contents indexed in addition to file properties&quot; is checked.<br /><br />5. In Classic Start Menu Settings, go to the Search Box tab. &quot;Search files&quot; and &quot;Search contents&quot; should be on.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Oct 13, 2015 12:56 am</p><hr />
