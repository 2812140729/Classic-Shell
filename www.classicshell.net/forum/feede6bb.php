Whichever buttons have icons, they point to icons stored in Windows DLLs. For those buttons which have blank icons, it means you must supply the icon yourself. After you add them by double clicking or dragging from right column to left column, double click the blank item in the left column and an &quot;Edit menu item&quot; dialog will come up. There you can specify how the button looks: its icon, or text label and tooltip which you may need to fill. The &quot;Command&quot; button is already prepopulated so the buttons are working, but just missing their icons.<br /> <br />You can find free icons on the web on sites like IconFinder.com, IconArchive.com etc.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Apr 25, 2013 6:54 pm</p><hr />
