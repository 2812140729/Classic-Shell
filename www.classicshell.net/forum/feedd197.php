What do you mean Start button doesn't work at all? You don't see it or clicking it takes to Metro? Did you clean install Windows 8 or upgraded from Windows 7? Somehow it looks like your Start button got turned off. By opening the menu using Ctrl+Esc, go to All Programs -&gt; Classic Shell -&gt; Start Menu settings. Click All Settings radio button, then click 'Start Button' tab and make sure &quot;Enable Start button&quot; option is checked. Log off and log back in and see if the Start button returns.<br />If you see the Start button but it is taking you to Metro, from Settings, go to Controls tab and change &quot;Left click opens&quot; to Classic Start Menu.<br /><br />Also, if it still doesn't show, have you any other Start menu replacement installed?<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Jan 13, 2013 7:54 am</p><hr />
