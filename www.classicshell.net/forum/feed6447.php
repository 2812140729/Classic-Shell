If the setting is &quot;bold&quot; it means that it is customized, even if the value is equal to the default value. Being default and being equal to the default are not the same thing.<br /><br />Imagine for example the color setting. When the setting is default, it will use the color of the OS. That color can change when the OS decides to change it. If the setting is not default but matches the current OS color, it will stay the same regardless of what the OS changes it to.<br /><br />Also from time to time I change the default value in new versions of Classic Shell. If you customized a setting it will stay that way after the upgrade. Otherwise it will get the new default from the new version.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Thu Jan 12, 2017 5:26 pm</p><hr />
