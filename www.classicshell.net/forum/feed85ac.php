There are always bugs being fixed in any new release of Classic Shell so yes that is one advantage.<br /><br />The taskbar in Windows 8.1 is transparent by default and Classic Shell has a setting to make it opaque (which I presume you have enabled) but it works only as long as the Start Menu is running. If the Start menu exits or if for some reason, Explorer.exe crashes, the taskbar will become transparent *momentarily* but as soon as Classic Start menu automatically loads again for Explorer.exe, it should turn opaque. If it remains transparent, you must manually launch Classic Start Menu from C:\Program Files\Classic Shell\ClassicStartMenu.exe. There should be no need to reboot Windows.<br /><br />The following new features have been added for Windows 8.1 after Classic Shell 4.2.1:<br /><br />- The Metro skin can have transparency if you are using the classic style of the menu<br />- New setting to align the custom start button to the corner instead of the vertical middle of the taskbar<br />- New command to access the PC settings directly<br />- The Start Menu has the ability now to move itself, for example, when the Windows 8/8.1 and Windows 10 touch keyboard pops up in the docked state or when the resolution changes (like when you exit a fullscreen game with the Win key)<br />- You can right-click on a newly installed/highlighted program and select &quot;Remove highlight&quot;<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Jul 27, 2015 5:15 am</p><hr />
