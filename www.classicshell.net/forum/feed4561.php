Just as a side note, Button Size 0 will give you its original size while any other value will scale it to that width (and height, proportionally) in pixels. I for my part have size 48 in use.<br /><br />And as a second note in case you're wondering about potentially weirdish behaviour of your button:<br /><br />There are three states: Normal, mouseover, pressed. Since these all have to be in the same image, the height of it is just split into three equal parts. So if you have a 99 pixel high image, the first 33 are the normal, the next 33 the mouseover and the last 33 the pressed state that is going to be displayed.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=254">vintageologist</a> — Sat Mar 09, 2013 1:51 am</p><hr />
