You can choose to not install Classic Explorer during setup. Or if Classic Explorer has already been installed, then start the installer for the same version again and choose &quot;Change&quot; and click Next. Click the Drive icon to the left of &quot;Classic Explorer&quot; in that dialog and choose &quot;Entire feature will be unavailable&quot;.<br /><br />Or you can have Classic Explorer installed but disabled by going to Internet Options -&gt; Programs tab -&gt; Manage Add-ons and disabling Explorer related addons in that dialog.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sat May 25, 2013 5:30 am</p><hr />
