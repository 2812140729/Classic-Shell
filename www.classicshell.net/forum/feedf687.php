Are you using a Microsoft account or local account in Windows 8? If using a Microsoft account (online credentials), the picture can be changed from Live Messenger or by going to <!-- m --><a class="postlink" href="http://profile.live.com">http://profile.live.com</a><!-- m -->.<br />If you are using a local account on Windows 8, open PC Settings. Go to &quot;Personalize&quot; section in PC Settings, then click 'Account Picture', click Browse and set the picture. To get more user pictures to show up in the PC Settings Browse... file picker, paste your images in %appdata%\Microsoft\Windows\AccountPictures.<br /><br />Also, make sure this Group Policy is NOT enabled on the machine: Computer Configuration-&gt;Administrative Templates-&gt;Control Panel-&gt;User Accounts-&gt;Apply the default user logon picture to all users. If that policy is enabled, then the option to change the user picture might be greyed out in 'PC Settings'. Or you could just use that policy to change the user picture (but it will change for all users on that PC).<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Jun 14, 2013 7:34 am</p><hr />
