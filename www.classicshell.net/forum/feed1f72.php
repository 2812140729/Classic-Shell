The new version 4.0.4 of Classic Shell is out. There is a new hidden setting added in Classic Shell 4.0.4 to change the All Programs width of the Start Menu in Windows 7 style: HKCU\Software\IvoSoft\ClassicStartMenu\Settings. DWORD value: ProgramsWidth. The value data should be entered in decimals. Delete the value to revert to the default programs width.<br /><br />The same setting is also there in Classic Shell's Group Policy settings: &quot;Programs pane width&quot;.<br /><br />You must exit the Start Menu and start it again from C:\Program Files\Classic Shell\ClassicStartMenu.exe after changing the setting.<br /><br /><br />For the Classic style, the Main menu width is still only an upper limit and the minimum width is determined by how long the names of the items are in the menu.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Jan 20, 2014 2:19 am</p><hr />
