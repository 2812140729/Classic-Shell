This is a really wierd issue..<br /><br /><br />It sounds like 1 of 4 things<br />A: you have some kinda 'time capsule' software: schools often use this to ensure that students dont remove programs from their computers (you probably wouldn't be able to permanently install/uninstall anything)<br />B: A virus is reverting changes made on restart (somewhat unlikely tho)<br />C: its another piece of software you are mistaking for classic shell? (probably the most likely)<br />D: some wierd write permissions are preventing the system from deleting the program <br /><br />If it is classic shell, the files are all stored at: C:\Program Files\Classic Shell by default (unless you specified elsewhere during install)<br />Does this folder exist, and can you delete its contents. This is by no means a clean uninstall.. but it should get the job done...<br />If you can delete its contents, but it re-appears after a restart, it is most likely some program restoring it.<br />Also if it really is classic shell, and all else fails, you can right click, go to settings, and from the 'basic settings' (or 'controls' tab if you have 'show all settings' enabled)<br />Find and change the following options:<br />&quot;Left-click opens&quot;<br />'Start button opens'<br /> to 'windows start menu'<br /><br />This wont uninstall classic shell, but instead tell classic shell to bypass its own menu in favor of the default.<br />Then the explorer toolbar component can be disabled by right clicking and unchecking 'classic explorer toolbar'<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=62">Jcee</a> — Sat Jan 27, 2018 9:51 pm</p><hr />
