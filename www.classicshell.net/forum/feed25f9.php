Here's how to add a folder containing EXEs to the system path to the Start Menu's search box will search it: Open Advanced System Properties (SystemPropertiesAdvanced.exe), Environment Variables. Under &quot;System Environment variables&quot;, locate the PATH value and double click to edit it. Add the folder whose EXEs you want to be searched after a semicolon to the end of the path variable without erasing its existing values. e.g. if Path variable is already C:\Windows, make it C:\Windows\;C:\XYZFolder\   (end it with a backslash).<br /><br />You can also create aliases for programs using Winaero's Win+R Alias Manager: <!-- m --><a class="postlink" href="http://winaero.com/comment.php?comment.news.76">http://winaero.com/comment.php?comment.news.76</a><!-- m --><br />E.g. MSP for Paint, WP for WordPad etc. In the Start Menu's search box, these aliases also work like they work in the Win+R (Run) dialog.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon May 20, 2013 12:01 pm</p><hr />
