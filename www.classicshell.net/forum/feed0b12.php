To add Admin Tools, here are the step-by-step instructions:<br /><br />1. Open settings, go to Customize Start Menu tab.<br />2. Scroll all the way down in right column, locate &quot;Custom Command&quot;, double click to add it or drag it. Position it up/down where you want it.<br />3. Now double click &quot;CustomItem&quot; in left column, from the &quot;Link&quot; field dropdown, choose Administrative Tools.<br />4. Assign an icon if you want, label, tooltip - all optional. Set options as you want like &quot;Don't Expand&quot; if you want to show it as a Link, not an expanding menu. Click OK twice.<br /> <br />Were you able to add Administrative Tools to the right column of the menu? <img src="http://www.classicshell.net/forum/images/smilies/icon_e_smile.gif" alt=":)" title="Smile" /><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Feb 06, 2013 6:01 am</p><hr />
