Yes it's possible. There are 2 ways to do this. First make sure that the setting &quot;Pinned Programs folder&quot; on the Main Menu tab of Start menu settings is set to &quot;Use Start Menu folder&quot; and not the &quot;Pinned folder&quot;. Then go the General Behavior tab and make sure that &quot;Expand folder shortcuts&quot; option is checked. Click OK to save settings. Then, either:<br /><br /><br />1. Drag and drop the folder you want pinned over to the Start button, wait till the menu opens and then pin it by dropping it inside the menu.<br /><br /><br />OR instead of drag and drop, you can use copy-paste.<br />2. Right click the folder you want pinned and click Copy. Then open %AppData%\Microsoft\Windows\Start Menu. Right click in an empty area of the Start Menu folder and click Paste shortcut.<br /><br />Both methods work only for the Start Menu folder, not for the Pinned folder.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Nov 07, 2013 9:32 am</p><hr />
