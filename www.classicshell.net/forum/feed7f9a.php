The keyboard shortcuts for toggling Preview Pane are Alt+P for Windows 7 and Windows 8/8.1. For the Details pane, it's Shift+Alt+P (Windows 8/8.1 only).<br /><br />Microsoft doesn't allow any way to programmatically toggle them but I created a script that sends keystrokes using the freeware, AutoHotkey. I have attached the plain text script (which requires AutoHotkey installed) and the compiled standalone EXE version of the script (which doesn't require AutoHotkey to be installed) to this post. Download it and give it a try. <br /><br /><br /><strong>Note that the EXE version even if it's safe will be blocked by Windows Defender/Microsoft Security Essentials as these 2 anti-malware apps are incompatible with AutoHotkey EXE compiled scripts and cause CPU hangs.</strong> You will have to add an exception to whitelist the file in MSE/Defender to prevent CPU usage from massively shooting up and hanging. If you have disabled Defender/MSE and use any other anti-malware, then the EXE version will run fine. So don't run the EXE version before adding an exception to Defender/MSE. If you don't want to use the EXE version (which is completely safe to use with other anti-malware apps), you can use the AHK version which only requires AutoHotkey to be installed.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Nov 04, 2013 8:40 pm</p><hr />
