Two Tone (Classic Style) <br /><br />Made for WINDOWS 10 with V4.2.4 <br />Can work in Windows 7, with settings adjustment in CSM. See below <br /><br /><br /><br />After downloading file, paste in skins folder. Usually C:\Program Files\Classic Shell\Skins <br />Exit CSM, restart CSM, and go to &quot;Start Menu Style&quot; tab. Pick &quot;Classic Style&quot;, or &quot;Classic with two columns&quot;.<br />Pick &quot;Two Tone&quot; in Skin tab. <br /><br />Back to simple. Not even any visible separators in this one, except in white submemu.<br />I decided to bring &quot;Dark Clouds&quot; skin out of mothballs, <br />and replace the solid preset colors, with glass colored blur instead.<br /><br />Used old fashioned glass color levels, so it's not going to match the taskbar exactly.<br />The grey matter having trouble grasping the metro color table ways.<br />You can still get it close though.<br /><br />Make sure to turn on glass for menu, and glass taskbar in CSM Win10 settings. <br /><br />I found there are two glass color intensity settings for the task bar.<br />Turning on or off Windows &quot;personalization settings/colors/make start, taskbar, and action center&quot;<br />It is still overridden as glass, by CSM. <br />So you can get it roughly the same glass match, by swtching between light/medium and<br />dark settings, in skin options<br /><br /><br />Heres an interesting try out. ( Wax paper ):) <br />Put on &quot;Black text&quot;. <br />Put on &quot;Light/medium variation&quot;.<br />Turn on &quot;Overide glass color&quot; in CSM settings, with &quot;Menu glass color&quot; FFFFFF<br /><br /><strong>Windows 7</strong><br />I double checked and saw that in &quot;Menu look&quot;,<br />&quot;Override glass color&quot; setting is still in CSM settings, for Win7, <br />and there is a good chance, it is in Win8.1 CSM settings too.<br />Thought setting got axed.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=8