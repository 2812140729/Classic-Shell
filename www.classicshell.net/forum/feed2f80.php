You can add any custom item to the Start Menu. Go to the Settings dialog and open the Customize Start Menu tab. Locate the &quot;Custom Command&quot; item at the bottom of the right column. Drag it to the left column and position it in the location you wish. This will create a new item named CustomItem. Double-click on the new item. Pick 'Administrative Tools' from the Link dropdown list, or click on the [...] button to select another folder. Change the label or the icon if you wish. Select &quot;Don't Expand&quot; if you wish the folder to not be expandable as a sub-menu.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Jan 08, 2013 6:43 am</p><hr />
