<content type="html" xml:base="http://www.classicshell.net/forum/viewtopic.php?t=1139&amp;p=5946#p5946"><![CDATA[
You can open any skin in Resource Hacker and edit the value of &quot;Main2_text_padding&quot; to adjust the spacing of items in the right column in a 2 column menu. Column break marks the start of the second column. Column padding moves the items below it to the bottom of the column.<br /><br />Main2_text_padding has 4 values corresponding to the padding for left/top/right/bottom. You will need to adjust top and bottom padding to increase the vertical spacing and then save the skin. The skin is best edited by copying it to some location like the Desktop and then copying it back to C:\Program Files\Classic Shell\Skins. It is advisable to rename the modified skin so it doesn't get overwritten when you upgrade to a new version of Classic Shell.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Aug 21, 2013 9:57 pm</p><hr />
