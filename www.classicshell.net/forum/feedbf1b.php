Over time, the menu remembers which items have been launched from the search and presents them first. If you launch Word a couple of times, it should become the top-most.<br /><br />You can't re-order search results but there are a couple of tweaks you can do.<br /><br />Open Classic Start Menu Settings and go to the Search Box tab. A few settings there are:<br /><br />- <strong>Track frequency of use</strong>: Unchecking this will turn off sorting of search results by usage. If you uncheck it and check it again, the frequency data related to search, stored by the menu will be reset. This should make Word the top-most result, and most definitely after launching it a couple of times.<br /><br />- <strong>Search the system path</strong>: Unchecking this will turn off searching of executable files located in the system path e.g. C:\Windows\system32\pass<strong>word</strong>onwakesettingflyout.exe. So only shortcut files will be searched, not EXE and other executable files.<br /><br />- <strong>Match parts of words</strong>: Unchecking this will turn off matching of middle of words. So, typing 'word' won't return 'password...'<br /><br />You can do either of these tweaks to control the search results.<br /><br />On my PC, Word is always the top-most result:<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Sun Oct 08, 2017 4:27 am</p><hr />
