As you may know, some programs like uTorrent like to add themselves in the root of &quot;Start Menu&quot; folder, and not in the &quot;Programs&quot; directory. This is rare, but it kinda corrupts Classic Shell's interface (any version). I will show screenshots.<br /><br />Here you can see a perfectly configured Start Menu:<br /><br /><a href="http://i43.tinypic.com/54acdc.jpg" class="postlink">http://i43.tinypic.com/54acdc.jpg</a><br /><br />And here you can see what happens when any program adds a shortcut to the root of &quot;Start Menu&quot; folder:<br /><br /><a href="http://i43.tinypic.com/2r3kos8.jpg" class="postlink">http://i43.tinypic.com/2r3kos8.jpg</a><br /><br />It causes Start Menu <span style="text-decoration: underline">to stretch</span> (I highlighted the problem with red marker). The more shortcuts get added, the more stretched Start Menu becomes. Yes, you can simply move shortcuts into &quot;Programs&quot; folder manually, but it would still be nice to have this interface problem corrected, because by default, it will stretch on and on forever.<br /><br />The only suggestion that comes to mind would be to remove the upper category from start menu, and make Classic Shell show its shortcuts in &quot;Programs&quot; menu instead, like this:<br /><br /><a href="http://i40.tinypic.com/28u22l4.jpg" class="postlink">http://i40.tinypic.com/28u22l4.jpg</a><br /><br />In fact, Windows XP did just that.<br /><br />It doesn't have to be this way by default, but it would be very nice to have that option to avoid that dreaded Start Menu stretching.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=659">supraspecies</a> — Mon Jul 29, 2013 2:26 pm</p><hr />
