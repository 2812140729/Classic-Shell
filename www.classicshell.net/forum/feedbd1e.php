The alignment is under control of Windows. First remove all the extra separators on the left. Then open any folder like Control Panel or Network Connections which has a menu bar and not the Ribbon. If the Menu Bar is hidden, press F10 to show it. Right click the toolbar/menu bar area and unlock the toolbars, and move the Classic Explorer toolbar to the right along the same row as the menu bar. Then navigate from that Explorer window to a location where Explorer shows the Ribbon like C:\ for example and lock the toolbar. Thereafter, for Ribbon-enabled folders, it should stay on the right.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Mon Sep 12, 2016 12:14 am</p><hr />
