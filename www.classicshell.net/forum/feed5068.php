You can add a folder with subfolders on the Explorer toolbar as follows:<br /><br />1. Open Classic Explorer Settings. Go to Toolbar Buttons tab.<br />2. Drag a custom command from right pane to the left pane, position it where you want and double click it to bring up the Edit Toolbar Item dialog. In the Link field, type the path of the folder you want to add. e.g. C:\Favorites or C:\Users\Windows\Links. It must be a folder on the file system, which contains the subfolders or shortcuts to folders. Leave the &quot;Command:&quot; field empty if you don't want a split button.<br />3. Pick any icon for it optionally. The Favorites icon is in C:\Windows\system32\imageres.dll, 1024.<br />4. Optionally enter the Label and Tip if you want them to show on the toolbar. Click OK to save settings.<br /><br />This will add a button to the Explorer toolbar.<br /><br /><img src="http://www.classicshell.net/forum/download/file.php?id=620" alt="" /><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Nov 03, 2015 8:52 pm</p><hr />
