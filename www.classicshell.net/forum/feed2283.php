Hello,<br />go to CSM Settings &gt; Customize Start Menu and move/delete the Programs item (on the top of list) to the right.<br /><br />Be aware the user can still override this themselves as CSM settings are per-user. To block this use Group Policies on the computer, have a look at this:<br /><!-- l --><a class="postlink-local" href="http://www.classicshell.net/forum/viewtopic.php?t=269">viewtopic.php?t=269</a><!-- l --><br /><br />Probably a good idea to remove other items too, like Run and Settings. So the only things on the menu will be the pinned and recent programs.<br /><br />However, also know the user can open the standard Win10 menu by pressing Shift+Win key. You can override this in Controls tab. Maybe just set Windows-key opens nothing, left-click opens nothing, Shift+Win, Shift+Click the same etc.<br /><br />Don't underestimate the computer skills of children, if there is a way around they will find it eventually <img src="http://www.classicshell.net/forum/images/smilies/icon_e_smile.gif" alt=":)" title="Smile" /><p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=27191">Pathduck</a> — Tue Nov 21, 2017 3:30 pm</p><hr />
