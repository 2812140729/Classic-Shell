The start menu process is C:\Program Files\Classic Shell\ClassicStartMenu.exe. Run it with the -exit parameter to exit it. You will need to supply the full path to the EXE in double quotes followed by the -exit parameter to exit it. Also, before relaunching it, a sleep timeout of 2-3 seconds is recommended.<br /><br />The beta is not that buggy actually. <img src="http://www.classicshell.net/forum/images/smilies/icon_e_biggrin.gif" alt=":D" title="Very Happy" /> It was very thoroughly tested while it was a private beta.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Jul 25, 2013 3:52 am</p><hr />
