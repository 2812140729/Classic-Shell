The Classic Start Menu should start by default on Windows 7 for all users, regardless of Standard user privileges or Administrator.<br /><br />Go to C:\Program Files\Classic Shell and start ClassicStartMenu.exe. Then right click the Start button -&gt; Settings -&gt; Check &quot;Show all settings&quot;. Go to the tab called General Behavior and check &quot;Start automatically for this user&quot;.<br /><br />Also open the Properties of the file, C:\Program Files\Classic Shell\ClassicStartMenu.exe and from the Compatibility tab, make sure that it is not set to Run as administrator.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Apr 04, 2014 11:10 pm</p><hr />
