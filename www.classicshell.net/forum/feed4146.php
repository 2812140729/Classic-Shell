The rules of what to show in the frequently used list are quite complex. In a nutshell, only programs with shortcuts in the start menu are shown, and only after you use that shortcut from the start menu at least once. After that the frequency of use is tracked regardless if you start the program from the start menu, the desktop, quick launch, or whatever. Programs pinned to the taskbar or the start menu are ignored.<br />That's how the system start menu behaves in Windows 7.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Sat Aug 10, 2013 8:43 pm</p><hr />
