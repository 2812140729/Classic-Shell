These are jump lists and are maintained by Windows. To remove an individual item from this list, right click on it and choose &quot;Remove from this list&quot;. To remove all items, right click Taskbar -&gt; Properties -&gt; Start Menu tab (on Windows 7) or Jump Lists tab (on Windows 8). Uncheck the option &quot;Store and display list of recently opened files (Windows 7)&quot; or &quot;Store and display list of recently opened items in Jump Lists (Windows 8)&quot;.<br /><br />If you want Windows to stop maintaining recently opened documents for only a specific extension/file type, follow this guide: <!-- m --><a class="postlink" href="http://www.officehackery.com/2009/11/hide-specific-file-types-from-windows.html">http://www.officehackery.com/2009/11/hi ... ndows.html</a><!-- m --> (Requires a free tool from NirSoft called FileTypesMan which can easily set the registry values).<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Thu Feb 21, 2013 8:47 am</p><hr />
