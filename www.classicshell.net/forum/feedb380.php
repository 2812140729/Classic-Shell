Classic Shell is one of the fastest apps and Ivo has done a great amount of work to make it as responsive as possible. It can be any reason - you might be starting to type too soon or your PC might be slightly slower. After pressing Win key, the menu can be made to open faster on slower computers by disabling frequent programs and highlighting. You could also turn off display of Metro apps in the main menu. Besides these, there is nothing that can be done to open it even quicker.<br /><br />By upgrading to a faster PC you can experience a great boost in performance, although even on my 8-9 year old laptop from 2007-2008, Classic Start Menu is extremely quick to open with all features enabled.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Feb 19, 2016 7:59 pm</p><hr />
