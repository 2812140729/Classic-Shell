Classic Start Menu supports several ways of adding custom items of your choice. One way is to right click the Start button, click Open or Open All Users and paste or create shortcuts inside the Programs folder. You could create a Video Apps folder there in Explorer, and drag shortcuts there.<br /><br />Another way is to add a Custom Command from the &quot;Customize Start Menu&quot; tab. The Custom Item can be a Link to a file or folder or a Command with parameters.<br /><br />A third way to add items is to directly drag them over the Start button, wait for the menu to open and drop them where you want to add them. So you could also create a folder anywhere with shortcuts to Video apps and then just drag that folder to the Start menu.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed Feb 06, 2013 12:23 pm</p><hr />
