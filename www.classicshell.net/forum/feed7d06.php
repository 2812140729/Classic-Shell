I'd rather not have Classic Shell track custom taskbars. The code will try to subclass them and will assume specific class names, window hierarchy and behavior. I cannot guarantee that it will work with custom implementations.<br /><br />The recommended way of doing this is to hook the creation of new windows and wait for a start menu to be created (a window with class ClassicShell.CMenuContainer). Then send it a message ClassicStartMenu.StartMenuMsg with the correct StartMenuParams structure. This will ensure that the menu is positioned correctly, both in screen space and in the Z order.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=2">Ivo</a> — Mon Dec 02, 2013 12:28 am</p><hr />
