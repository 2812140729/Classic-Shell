Version 3.6.8 is the last one which runs on Windows Vista. You can download it here: <a href="http://sourceforge.net/projects/classicshell/files/latest/download" class="postlink">Classic Shell 3.6.8 installer.</a> Version 4.x and above run on Windows 7 and Windows 8.<br /><br />Yes it's possible to have what you describe on Vista/Windows 7/Windows 8 too.<br />1. First install Classic Shell, including the Classic Explorer component.<br />2. Open Classic Explorer settings and turn on the option to show All Settings. (In 3.6.8, it's a radio button at the bottom). In Classic Shell 4.x, it's a checkbox at the top of the Settings dialog.<br />3. Now go to the Navigation Pane tab and choose the 'Windows XP Simple' style. This style doesn't have the dotted lines and behaves exactly like Windows XP Simple Folder View.<br />4. Check the option &quot;Don't fade buttons&quot; so that the arrows/+/- boxes don't disappear when you mouse over to the right pane.<br />5. Click OK and then close all Explorer windows (if you have them open) and reopen them.<br /><br />You should have +/- checkboxes in the left pane now and the +/- buttons won't disappear. If it doesn't work, then it means all the Explorer addons added by Classic Shell aren't enabled. Sometimes IE automatically disables Classic Shell addons (that's because Internet Explorer and Windows Explorer share a common addon model. If they are disabled, then please follow the instructions here to enable all of them: <!-- m --><a class="postlink" href="http://www.classicshell.net/faq/#explorer_toolbar">http://www.classicshell.net/faq/#explorer_toolbar</a><!-- m -->) and then do the above steps.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Jul 04, 2014 10:03 pm</p><hr />
