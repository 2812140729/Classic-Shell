This is not related to Classic Shell. Title bars and window corners are part of the Windows theme you use which is restricted by Microsoft. They allow using only built-in themes by digitally signing them. To use third party themes which are not signed, you must install a free app like UXStyle (<!-- m --><a class="postlink" href="http://www.uxstyle.com">http://www.uxstyle.com</a><!-- m -->) which unlocks use of third party themes.<br /><br />Then, you need a third party theme that is made for the exact Windows version you use. You can find a theme with rounded corners at sites like deviantart.com. Note that it must be made specifically for the version of Windows that you use. Windows 8.1 theme cannot be used on Windows 8 or 7. Windows 8.1 Update 1 theme won't work correctly on Windows 8.1 either.<br /><br />Note that this is for experienced users only. Install third party tools and skins at your own risk. Classic Shell is not responsible for anything that happens to your Windows installation from use of third party customizations.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Tue Apr 28, 2015 5:34 pm</p><hr />
