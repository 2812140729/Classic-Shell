You can already do that.<br />1. Right click the Start Button -&gt; Settings -&gt; All Settings. Go to Customize Start Menu tab.<br />2. In the right column, scroll all the way down and drag &quot;Custom Command&quot; to the left column, give it some name by renaming e.g.VideosLibraryItem<br />3. Double click Custom Item and in the LINK field dropdown, choose Documents, Pictures, Music or Videos (those locations without the 'My' prefix are the Libraries). Click OK.<br />Repeat these steps 1-3 to add all 4 Libraries.<br />Tip: In the left column you can drag to position and reorder items as you want. You can also select UserDocumentsItem, UserPicturesItem and press the Del key on the keyboard to remove them and keep only Libraries.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Wed May 08, 2013 8:23 pm</p><hr />
