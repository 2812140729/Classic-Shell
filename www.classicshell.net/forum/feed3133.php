%computername% is the environment variable you need to use. Just put it wherever you want. There are 3 places to put it - 'Menu caption', 'User name text' (both on Menu Look tab) and on the Start button tab, you can put %computername% in the 'Button tooltip'. If you want it in the caption including the OS name, just put the OS name followed by %computername%.<p>Statistics: Posted by <a href="http://www.classicshell.net/forum/memberlist.php?mode=viewprofile&amp;u=54">Gaurav</a> — Fri Jan 18, 2013 1:24 am</p><hr />
